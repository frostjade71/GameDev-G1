    public static function getWordsByDifficulty($difficulty) {
        $words = self::getVocabularyWords();
        return array_filter($words, function($word) use ($difficulty) {
            return $word['difficulty'] == $difficulty;
        });
    }
    
    public static function getWordsByGrade($grade) {
        $words = self::getVocabularyWords();
        return array_filter($words, function($word) use ($grade) {
            return $word['grade'] == $grade;
        });
    }
    
    public static function getRandomWord($difficulty = null, $grade = null) {
        $words = self::getVocabularyWords();
        
        if ($difficulty !== null) {
            $words = array_filter($words, function($word) use ($difficulty) {
                return $word['difficulty'] == $difficulty;
            });
        }
        
        if ($grade !== null) {
            $words = array_filter($words, function($word) use ($grade) {
                return $word['grade'] == $grade;
            });
        }
        
        if (empty($words)) {
            return null;
        }
        
        return $words[array_rand($words)];
    }
    
    public static function searchWords($query) {
        $words = self::getVocabularyWords();
        $query = strtolower($query);
        
        return array_filter($words, function($word) use ($query) {
            return strpos(strtolower($word['word']), $query) !== false ||
                   strpos(strtolower($word['definition']), $query) !== false;
        });
    }
